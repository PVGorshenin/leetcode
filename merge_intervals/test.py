from pytest import mark
from merge_intervals import Solution


@mark.parametrize('intervals, answer', [
    ([[2,3],[4,5],[6,7],[8,9],[1,10]], [[1, 10]]),
    ([[2,3],[5,5],[2,2],[3,4],[3,4]], [[2, 4], [5, 5]]),
    ([[4,5],[2,4],[4,6],[3,4],[0,0],[1,1],[3,5],[2,2]], [[0, 0], [1, 1], [2, 6]]),
    ([[2,3],[2,2],[3,3],[1,3],[5,7],[2,2],[4,6]], [[1,3],[4,7]]),
    ([[2,2],[2,3],[4,6],[5,6],[1,2],[5,6],[1,3],[3,3]], [[1,3],[4,6]]),
    ([[1,1],[2,2],[0,0],[2,3],[1,3],[3,5],[2,3],[3,5]], [[0,0], [1,5]])
                                   ]
                  )
def test_jump(intervals, answer):
    assert Solution().merge(intervals) == answer